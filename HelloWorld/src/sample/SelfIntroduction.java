package sample;

public class SelfIntroduction {

	public static void main(String[] args) {
		// 氏名
		String name = "岡澤 亮太";
		// 年齢
		int age = 21;
		// 趣味
		String hobby = "映画鑑賞";


		/*
		* 自己紹介文を出力する処理
		*/

		System.out.println("私の名前は" + name + "です。");
		System.out.println("年齢は" + age + "歳です。");
		System.out.println("趣味は" + hobby + "です。");

	}

}
